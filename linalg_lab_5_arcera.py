import numpy as np
import matplotlib.pyplot as plt

L = np.array([[5, 3, 0],
              [3, 6, 0],
              [0, 4, 7]])


def plotgraph(l):
    fig = plt.figure()

    x1 = fig.gca(projection='3d')
    x1.set_xlim([0, 10])
    x1.set_ylim([0, 10])
    x1.set_zlim([0, 10])
    origin = (0, 0, 0)
    x1.quiver(origin, origin, origin,
               l[:, 0], l[:, 1], l[:, 2],
               arrow_length_ratio=0.05, colors=['purple', 'red', 'green'])
    plt.show()

plotgraph(L)