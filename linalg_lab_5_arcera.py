import numpy as np
import matplotlib.pyplot as plt
# %matplotlib inline


E = np.array([
              [4, 8], 
              [9, 3]
            ])

def plotgraph(l):
    plt.xlim(0, 15)
    plt.ylim(0, 15)
    plt.quiver([0, 0], [0, 0], l[:, 0], l[:, 1], 
               angles='xy', scale_units='xy', scale=1, color=['purple', 'blue'])
    plt.grid()
    plt.show()
plotgraph(E)

